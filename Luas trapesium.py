# Program untuk mencari luas Trapesium
a = float(input("Masukan panjang sisi sejajar pertama: "))
b = float(input("Masukan panjang sisi sejajar kedua: "))
t = float(input("Masukan tinggi trapesium: "))

# Menghitung luas trapesium dengan rumus 0,5 * (a + b) * t
L = 0.5 * (a + b) * t

# Menampilkan hasil luas trapesium
print("Luas Trapesium Adalah: ", L)